package it.geosolutions.geobatch.unredd.script.model;

/**
 *
 * @author ETj (etj at geo-solutions.it)
 */
public class GeoStoreConfig implements Cloneable {

    private String url;
    private String username;
    private String password;

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    @Override
    public String toString() {
        return "GeoStoreConfig["+username + " @ " + url +"]";
    }


    /**
     * Just get rid of CloneNotSupportedException
     */
    @Override
    public GeoStoreConfig clone() {
        try {
            return (GeoStoreConfig)super.clone();
        } catch (CloneNotSupportedException ex) {
            throw new IllegalStateException("Unexpected exception", ex);
        }
    }


}
